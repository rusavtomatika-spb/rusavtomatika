function get_kw($type) {
    global $TITLE;
    $path = $_SERVER['PHP_SELF'];
    $kw = $GLOBALS['kw'];

//$path="/index.php";

    for ($i = 0; $i < sizeof($kw); $i++) {

        $jmax = sizeof($kw[$i]["path"]);
        
        for ($j = 0; $j < $jmax; $j++) {

            if ($kw[$i]["path"][$j] == $path) {
                
                if ($type == "title"){
                    if (isset($TITLE)) return insert_name($TITLE);
                    return insert_name($kw[$i]["title"]);                
                }
                if ($type == "descr")
                    return insert_name($kw[$i]["descr"]);
                if ($type == "kw")
                    return insert_name($kw[$i]["kw"]);
                if (isset($kw[$i]["alt"]) and $type == "alt")
                    return insert_name($kw[$i]["alt"]);
            }
        }
    }


    return $path;
}

