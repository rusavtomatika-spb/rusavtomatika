<h3 id="FBD_connect_between_instructions">FBD connect between instructions</h3>
<p>FBD between instructions connected by enable flow line. </p>
<p>1. Click Tools bar <img src="<?= $path_to_images ?>images/fbd_line.gif"  /> button, mouse change to <img src="<?= $path_to_images ?>images/fbd_line_icon.gif"  />, mouse moce to instruction input or output item, if enable connect then mouse change to <img src="<?= $path_to_images ?>images/fbd_lineok_icon.gif"  />, if disable connect mouse change to <img src="<?= $path_to_images ?>images/fbd_lineno_icon.gif"  />.</p>
<p>2. Instruction output item can not connect to self input item, input item only a enable flow line input, output item may be output many enable flow lines.</p>
<p>3. Enable flow line connected will automatic change function block instruction executed sequence.</p>
<p>4. Between instruction may connect not use enable flow line, direct input instruction parameter.</p>
<p>5. Mouse click enable flow line will prompt the enable flow line detail connect information.</p>
