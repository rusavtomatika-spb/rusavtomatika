<h3 id="Interrupt_program">Interrupt program</h3>
<p>Interrupt program is a program which deal with system interrupt event. System assign a interrupt number for each interrupt event, refer to "
    <a href="/plc/haiwell-plc-programming-software/Appendix/#System_interruption_table">System interrupt table</a>".</p>
<p>1. Interrupt program executed must meet 2 conditions : one is use ATCH instruction binding the interrupt program and the interrupt event (interrupt number), two is system happen the interrupt event. Among, catch X0~X7 rising edge or falling edge interrupt must at "
    <a href="#PLC_hardware_configuare">PLC hardware configuration</a> " window "<a href="#X_digital_input_parameter">X digital input parameter</a>" open.</p>
<p>2. When system happen the interrupt event, program executed broke, program automatic jump to the interrupt event binded interrupt program executing, until interrupt program executed complete, system return to normal program executing.</p>
<p>3. User program may be without interrupt program, also may be include one or many interrupt program.</p>
<p>4. System interrupt controlled by ENI (interrupt enable )and DISI (interrupt prohibit) instruction, at interrupt prohibit, system can not happen interrupt event. Default is enable interrupt.</p>
<p>Note:interrupt program only executed once at corresponding interrupt event happen.</p>
