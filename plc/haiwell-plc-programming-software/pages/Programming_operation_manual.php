<p><img src="<?= $path_to_images ?>images/status%20bar.gif" /> </p>
<h3 id="Working_area">Working area</h3>
<p>User working area is use for write control program. configure PLC hardware information.At the area, user may be edit current program or PLC hardware configure information.</p>
<p><img src="<?= $path_to_images ?>images/work_space.gif" /> </p>
<h3 id="Project_manager">Project manager</h3>
<p>Via menu [Search/project manager] open "Project manager" page, it use tree form structure display total project all content: project name. main program block. sub program. interrupt program. table. PLC hardware configure. all kinds of instruction etc.. Project manager support right click menu, convenience user manage operate the project.</p>
<p><img src="<?= $path_to_images ?>images/project.jpg" /></p>
<h3 id="PLC_resource">PLC resource</h3>
<p>Via menu [Search/PLC resource] open "PLC resource" page, "PLC resource" page total 5 sub pages, list current program project CPU all resources of MPU, and system status bit. system register. interrupt and error code. </p>
<p><img src="<?= $path_to_images ?>images/plc_res.jpg" /></p>
<h3 id="Component_comment_table">Component comment table</h3>
<p><strong> </strong>Via menu [Search/component comment table]open "component comment table" page, it list current program project all component comment. </p>
<p><img src="<?= $path_to_images ?>images/addressnote_list.jpg" /></p>
<h3 id="Instruction_declare_window">Instruction declare window</h3>
<p>Via menu [Search/instruction declare window] open "instruction declare window" page, it list current pitched up instruction brief declare, convenience user understand the instruction. </p>
<p><img src="<?= $path_to_images ?>images/il_note.gif" /></p>
<h3 id="Instruction_attribute_window">Instruction attribute window</h3>
<p>Double click the application instruction in the program, can open the instruction attribute window, different content will be display according to different instruction. </p>
<p><img src="<?= $path_to_images ?>images/il_shuxing.jpg" /></p>
<p>1. When select "Instruction forbidden", the instruction can not be executed and display turn to grey.</p>
<p>2. When select "Low byte mode", the instruction is 8 bit mode; when select "32 bits", the instruction is 32 bit mode.</p>
