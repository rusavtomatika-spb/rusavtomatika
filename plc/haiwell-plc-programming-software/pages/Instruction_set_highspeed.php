<p> [Instruction function and effect declare] </p>
<p> 1.JOGP is the jog pulse output instruction. This instruction is executed when En = ON and the instruction is not executed when En = OFF.</p>
<p> 2.PulF is the pulse output frequency. If PulF?0, it will not output. If PulF is greater than 0 but less than the minimum frequency (10Hz), the output will be in accordance with the minimum frequency. If PulF is greater than the maximum frequency, the output will be in accordance with the maximum frequency. </p>
<p> 3.When the time of "FWD = ON" is less than 0.5s, a forward pulse will be output. When the time of "FWD = ON" is greater than 0.5s, a continuous forward pulse will be output at PulF frequency. </p>
<p> 4.When the time of "REV = ON" is less than 0.5s, a reverse pulse will be output, when the time of "REV = ON" is greater than 0.5s, then a continuous reverse pulse will be output at PulF frequency. </p>
<p> 5.When the FWD and REV are both ON, then no pulse output. </p>
<p> 6. There is no limit on the number of JOGP instructions. It can can coexist with other pulse instructions, but each pulse output channel can only execute one instruction at one time.</p>
