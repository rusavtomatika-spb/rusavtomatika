<p> [Instruction function and effect declare] </p>
<p> 1.MPTO is the multi-segment pulse output instruction, Tbl is segment start element, each segment occupies four registers, the first two registers are used for the frequency of the pulse output, the last two registers are used for the number of the pulse output .</p>
<p> 2. If the segment frequency is less than the minimum frequency (10Hz),then the output will be in accordance with the minimum frequency, if the segment frequency is greater than the maximum frequency,then the output will be in accordance with the maximum frequency output.</p>
<p> 3.If the segment pulse value is greater than 0, it means a forward pulse output. If the pulse value is less than 0, it means a reverse pulse. If the pulse value is 0, it means no output and it will generate a report of No.3 parameter error.</p>
<p> 4. When Tms = 0, it means no acceleration/deceleration; when Tms &lt;minimum acceleration/deceleration time,then it will use the minimum acceleration/deceleration time. When Tms&gt; maximum acceleration/deceleration time, then it will use the maximum acceleration/deceleration time.</p>
<p> 5. If there are frequency differences between the different segments, MPTO will make a smooth shift based on acceleration and deceleration time(Tms).</p>
<p> 6. MPTO instruction relate to <a href="#PLS-SM">SM system state bit</a>. <a href="#PLS-SV">SV system register</a>, and it will generate a pulse output interrupt. The interrupt "PLSx start pulse output" is generated when the pulse output is started. The interrupt "PLSx pulse output is ended" will be generated when the output pulse is finished after the instruction execution completed . </p>
<p> 7. When En= ON the instruction is executed,then Eno=On. When "En" changes from "ON" to "OFF",the instruction will slow down to stop according to the Tms (if the brake is not set in advance), then Eno=OFF.</p>
<p> 8. There is no limit on the number of MPTO instructions. It can can coexist with other pulse instructions, but each pulse output channel can only execute one instruction at one time.</p>
<p> 9.During the pulse output, the pulse frequency and pulse number of current segment and the next segment can not be modified (the modification will not take effect), and the other segment data can be modified in real time.</p>
