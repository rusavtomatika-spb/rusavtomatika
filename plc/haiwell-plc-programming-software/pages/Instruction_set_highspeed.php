<p> 2. When M8=OFF, it adds counting, when M8=ON,it reduces counting. </p>
<p> 3. When V50&gt;=V1000, M100=ON, while when V50&lt;V1000, M100=OFF .</p>
<p> 4. When M9=ON, it resets the counter, M100=OFF, V50=0.</p>
