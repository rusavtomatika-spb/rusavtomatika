<p> [High speed control instruction declare]</p>
<p> 1. Haiwell PLC high speed counter and high speed pulse output defined by channel ,related the hardware of the PLC ,Configured by "
    <a href="/plc/haiwell-plc-programming-software/Programming_operation_manual/#PLC_hardware_configuare">PLC hardware configure</a>". High speed counter channel signify by HSCx ,each channel use 2 high speed pulse input point .High speed pulse output channel signify by PLSx , each channel use 2 high speed pulse output point .</p>
<p> 2. High speed counter support :pulse/direction . positive/negative pulse . A/B phase pulse input model, support 1. 2. 4 frequency multiplication counting model , refer to" <a href="/plc/haiwell-plc-programming-software/Programming_operation_manual/#HSC_high_speed_counter_parameter">HSC high speed counter parameter</a>".</p>
<p> 3. High speed pulse output support : single pulse. pulse/direction . positive/negative pulse . A/B phase pulse . synchronization pulse output,refer to"<a href="/plc/haiwell-plc-programming-software/Programming_operation_manual/#PLS_high_speed_output_parameter">PLS high speed pulse output parameter</a>".</p>
<p> 4. Motion control support linear interpolation . circular interpolation . synchronization pulse output etc.; support absolute address . relative address ; support backlash compensation ;support electric origin point redefine etc..</p>
