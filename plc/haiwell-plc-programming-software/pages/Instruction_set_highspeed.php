<p> 2. PulN means the number of output pulses, PulN&gt; 0 indicates the positive pulse output , PulN &lt;0 indicates the reverse pulse output , PulN = 0 and when it's in relative address mode, indicates the pulses output continuously without counting the number of pulse.</p>
