<p> [Instruction function and effect declare] </p>
<p> 1. SHC instruction counts the high-speed impulse input of the Xn input point , it does not use the HSCx high-speed pulse input channel, one high-speed pulse input channel has two high-speed pulse input points. Therefore the host with eight pulse input channels can achieve the fucntion of 16 ways high-speed pulse input. </p>
<p> 2. SHC instruction is a 32-bit high-speed pulse counter, it does not generate high-speed counter interrupt, and it doesn't use the SM, SV.</p>
<p> 3. Dir terminal control the counting direction, when Dir = OFF it adds counting;while when Dir = ON,it reduces counting.</p>
<p> 4. Reset terminal controls the reset of the counter,when Reset = ON , it resets the counter.</p>
<p> 5. When "HVal" is greater than or equal to "PV" , "Out" is equal to "ON", when "HVal" is less than "PV", then "Out" is equal to "OFF".</p>
