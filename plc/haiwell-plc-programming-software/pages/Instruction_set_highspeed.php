<p> 7. DVIT instruction relate to <a href="#PLS-SM">SM system state bit</a>. <a href="#PLS-SV">SV system register</a>, moreover generate pulse output interrupt , instruction start executing pulse output general "PLSx start output pulse " interrupt , instruction executed complete stop pulse output general " PLSx output complete" interrupt .</p>
<p> 8. En=ON instruction executing ,Eno=ON; When pulse output process En go to OFF, then stop pulse output,Eno=OFF.</p>
<p> 9. DVIT instruction there is not number of branches ,can coexist with others pulse output instruction ,but each pulse output channel only one instruction at the same time .</p>
<p> 10. When the instruction is executing the pulse output, only if PulN=0 the MaxF can be modified in real-time, and in all other cases the Real-Time parameters can not be modified.</p>
