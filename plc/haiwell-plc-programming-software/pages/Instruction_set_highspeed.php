<p> 6.SYNP instruction does not have the limitation on the number, it can coexist with other pulse output instructions, but each pulse output channel can only execute one instruction at the same time.</p>
<p> 7.After this instruction is started, the parameters RMul and RDiv can be modified in real time.</p>
