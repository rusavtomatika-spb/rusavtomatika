function isRussianUser() {
    $userIp = getUserIP();
    
    if ($userIp == '127.0.0.1' || $userIp == '::1') {
        return true;
    }
    
    return checkIfRussianIP($userIp);
}

function getUserIP() {
    $ip = $_SERVER['REMOTE_ADDR'];
    
    $proxy_headers = [
        'HTTP_X_FORWARDED_FOR',
        'HTTP_X_REAL_IP',
        'HTTP_CF_CONNECTING_IP',
        'HTTP_CLIENT_IP'
    ];
    
    foreach ($proxy_headers as $header) {
        if (isset($_SERVER[$header]) && !empty($_SERVER[$header])) {
            $ip_list = explode(',', $_SERVER[$header]);
            $candidate = trim($ip_list[0]);
            
            if (filter_var($candidate, FILTER_VALIDATE_IP)) {
                if (!filter_var($candidate, FILTER_VALIDATE_IP, FILTER_FLAG_NO_PRIV_RANGE | FILTER_FLAG_NO_RES_RANGE)) {
                    continue;
                }
                $ip = $candidate;
                break;
            }
        }
    }
    
    return $ip;
}

function checkIfRussianIP($ip) {
    static $russianRanges = null;
    
    if ($russianRanges === null) {
        $russianRanges = loadRussianRanges();
    }
    
    $ipLong = ip2long($ip);
    if ($ipLong === false) {
        return false;
    }
    
    foreach ($russianRanges as $range) {
        $start = ip2long($range['start']);
        $end = ip2long($range['end']);
        
        if ($ipLong >= $start && $ipLong <= $end) {
            return true;
        }
    }
    
    return false;
}

